<h2 class="tisa1" style=" text-align:left; margin-top:22px;color:#000; ">MOTOR DRIVEN - Combination portable and fused style</h2>
<p>Designed for closing plastic bags, this unique device combines the effects of heat, pressure and cooling, to offer the many advantages of continuous band sealing.</p>

<h2 class="tisa1" style="text-align:left; margin-top:22px;color:#000; ">POSITIVE CLOSURE</h2>
<p>Continuously moving Teflon coated stainless steel bands support the film or bag top under moderate pressure through the complete sealing cycle - heating and cooling.</p>

<h2 class="tisa1" style=" text-align:left; margin-top:22px;color:#000; ">CONTROLLED TEMPERATURE</h2><p>Sealing temperature is closely controlled by a thermostate; pilot light indicates when the required temperature is reached.</p>

